def bdr():
    return Border(left=THIN, right=THIN, top=THIN, bottom=THIN)

def title_row(ws, row, text, bg=NAVY, fg=WHITE, sz=13):
    ws.merge_cells(f"A{row}:M{row}")
    c = ws.cell(row=row, column=1, value=text)
    c.font      = Font(name="Calibri", bold=True, color=fg, size=sz)
    c.fill      = PatternFill("solid", fgColor=bg)
    c.alignment = Alignment(horizontal="center", vertical="center")
    ws.row_dimensions[row].height = 32

def hdr(ws, row, texts, bgs=None):
    bgs = bgs or [MID_BLUE]*len(texts)
    for i, (t, bg) in enumerate(zip(texts, bgs), 1):
        c = ws.cell(row=row, column=i, value=t)
        c.font      = Font(name="Calibri", bold=True, color=WHITE, size=10)
        c.fill      = PatternFill("solid", fgColor=bg)
        c.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        c.border    = bdr()
    ws.row_dimensions[row].height = 22

def data_row(ws, row, vals, alt=False, bold_cols=None, center_cols=None,
             bg=None, fg="000000"):
    bg = bg or (GREY_LT if alt else WHITE)
    bold_cols  = bold_cols  or []
    center_cols= center_cols or []
    for i, v in enumerate(vals, 1):
        c = ws.cell(row=row, column=i, value=v)
        c.font      = Font(name="Calibri", size=10,
                           bold=(i in bold_cols), color=fg)
        c.fill      = PatternFill("solid", fgColor=bg)
        c.alignment = Alignment(
            horizontal="center" if i in center_cols else "left",
            vertical="center", wrap_text=True)
        c.border    = bdr()
    ws.row_dimensions[row].height = 18

def sec_hdr(ws, row, text, bg=MID_BLUE, ncols=13):
    ws.merge_cells(f"A{row}:{get_column_letter(ncols)}{row}")
    c = ws.cell(row=row, column=1, value=text)
    c.font      = Font(name="Calibri", bold=True, color=WHITE, size=11)
    c.fill      = PatternFill("solid", fgColor=bg)
    c.alignment = Alignment(horizontal="left", vertical="center", indent=1)
    c.border    = bdr()
    ws.row_dimensions[row].height = 22

def cw(ws, widths):
    for i, w in enumerate(widths, 1):
        ws.column_dimensions[get_column_letter(i)].width = w


