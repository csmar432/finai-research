def check_6_numerical_tests_pass() -> CheckResult:
    """Audit claim: 'Existing tests don't catch numerical regressions.'

    We run tests/test_numerical_correctness.py and verify all pass.
    """
    test_file = PROJECT_ROOT / "tests" / "test_numerical_correctness.py"
    if not test_file.exists():
        return CheckResult(
            passed=False,
            actual="test file missing",
            expected="test_numerical_correctness.py exists",
            evidence=[],
        )
    cmd = [sys.executable, "-m", "pytest", str(test_file), "-q", "--tb=no"]
    res = subprocess.run(cmd, capture_output=True, text=True, timeout=180, cwd=PROJECT_ROOT)
    # Parse last line for "N passed"
    summary = ""
    for line in res.stdout.splitlines() + res.stderr.splitlines():
        if "passed" in line and ("in " in line or "s)" in line):
            summary = line.strip()
    evidence = [f"  pytest exit: {res.returncode}", f"  summary: {summary}"]
    if res.returncode == 0:
        return CheckResult(
            passed=True,
            actual=f"all pass",
            expected="all pass",
            evidence=evidence,
        )
    return CheckResult(
        passed=False,
        actual=f"exit={res.returncode}",
        expected="exit=0",
        evidence=evidence,
    )


