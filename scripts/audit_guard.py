def check_9_ssot_numbers_match_docs() -> CheckResult:
    """Verify that PROJECT_NUMBERS.json (SSOT) matches the actual state.

    This is the root-cause fix for repeated number-hallucination audits:
    numbers drift when maintained in 5+ docs independently. The SSOT
    (scripts/PROJECT_NUMBERS.json) is the single source of truth; docs must
    be updated from it via sync_numbers.py --apply, not manually.
    """
    ssot_path = PROJECT_ROOT / "scripts" / "PROJECT_NUMBERS.json"
    if not ssot_path.exists():
        return CheckResult(
            passed=False,
            actual="SSOT file missing",
            expected="scripts/PROJECT_NUMBERS.json exists",
            evidence=[],
        )
    import json as _json
    ssot = _json.loads(ssot_path.read_text())

    # Verify MCP directory count
    import subprocess as _sub
    res = _sub.run(
        ["sh", "-c", f"ls {PROJECT_ROOT}/mcp_servers/ | grep '^user_' | wc -l"],
        capture_output=True, text=True,
    )
    actual_mcp = int(res.stdout.strip())
    ssot_mcp = ssot["mcp"]["total_directories"]
    mcp_ok = actual_mcp == ssot_mcp

    # Verify stub servers have tools/
    stub_issues = []
    for stub_name in ssot["mcp"].get("stub_servers", []):
        tools_dir = PROJECT_ROOT / "mcp_servers" / stub_name / "tools"
        if not tools_dir.exists():
            stub_issues.append(f"  {stub_name}: no tools/ directory")
        elif not list(tools_dir.glob("*.json")):
            stub_issues.append(f"  {stub_name}: tools/ is empty")
    stub_ok = len(stub_issues) == 0

    evidence = [
        f"  SSOT MCP directories: {ssot_mcp}, actual: {actual_mcp} → {'OK' if mcp_ok else 'MISMATCH'}",
        f"  SSOT stubs: {len(ssot['mcp'].get('stub_servers', []))}, actual stub issues: {len(stub_issues)}",
    ]
    if stub_issues:
        evidence.extend(stub_issues[:5])

    all_ok = mcp_ok and stub_ok
    return CheckResult(
        passed=all_ok,
        actual=f"MCP match={mcp_ok}, stubs={len(stub_issues)} issues",
        expected="SSOT matches actual state",
        evidence=evidence,
    )


def check_11_omit_longtail_not_growing() -> CheckResult:
    """Audit claim (audit-2026-07-04 P0-1): 'CI 覆盖率凑数通过新增 omit 藏分母'.

    Defense: bound the size of [tool.coverage.run] omit list. If it
    grows beyond a known set of legitimate CLI / DEPRECATED modules,
    this is a hallucinated-fix attempt and must be rejected.
    """
    p = PROJECT_ROOT / "pyproject.toml"
    if not p.exists():
        return CheckResult(False, "pyproject.toml missing", "exists", [])
    text = p.read_text()
    block_start = text.find("[tool.coverage.run]")
    if block_start < 0:
        return CheckResult(False, "no [tool.coverage.run] block", "present", [])
    rest = text[block_start:]
    # Stop at next TOML section header (line starting with `[name]`).
    block_lines = []
    lines = rest.splitlines(keepends=True)
    for idx, ln in enumerate(lines):
        if idx == 0:
            block_lines.append(ln)
            continue
        s = ln.strip()
        if s.startswith("[") and s.endswith("]") and not s.startswith("#"):
            break
        block_lines.append(ln)
    block = "".join(block_lines)
    in_omit = False
    omit_lines = []
    for ln in block.splitlines():
        s = ln.strip()
        if s.startswith("omit"):
            in_omit = True
            continue
        if in_omit:
            if s.startswith("]"):
                in_omit = False
                continue
            if s.startswith('"') and ".py" in s:
                omit_lines.append(s)
    count = len(omit_lines)
    # 2026-07-04 baseline (post PR-1): ~24 CLI/DEPRECATED entries.
    # Cap at 29 to allow legitimate additions; bigger is suspicious.
    cap = 29
    evidence = [f"  omit entries: {count}", f"  cap: {cap}"]
    if omit_lines:
        evidence.extend(f"    -> {x[:60]}" for x in omit_lines[:3])
    return CheckResult(
        passed=count <= cap,
        actual=f"{count} entries",
        expected=f"<= {cap}",
        evidence=evidence,
    )


