PROJECT_ROOT = Path(__file__).resolve().parent.parent


@dataclass
class AuditCheck:
    """Single verifiable audit claim."""

    id: int
    title: str
    description: str
    run: Callable[[], "CheckResult"]


@dataclass
class CheckResult:
    """Outcome of verifying a single audit claim."""

    passed: bool
    actual: str
    expected: str
    evidence: list[str] = field(default_factory=list)


