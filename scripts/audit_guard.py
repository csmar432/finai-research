def main() -> int:
    parser = argparse.ArgumentParser(
        description="Defensive guard against audit hallucinations"
    )
    parser.add_argument("--check", help="comma-separated check IDs (default: all)")
    parser.add_argument("--json", action="store_true", help="JSON output")
    parser.add_argument(
        "--update-baseline",
        action="store_true",
        help="Update expected state file (for legitimate baseline changes)",
    )
    args = parser.parse_args()

    if args.check:
        ids = {int(x) for x in args.check.split(",")}
        selected = [c for c in CHECKS if c.id in ids]
    else:
        selected = CHECKS

    results: list[tuple[AuditCheck, CheckResult]] = []
    for check in selected:
        print(f"\n[Check {check.id}] {check.title}")
        print(f"  {check.description}")
        try:
            result = check.run()
        except Exception as e:
            result = CheckResult(
                passed=False, actual=f"error: {e}", expected="verifiable", evidence=[str(e)]
            )
        status = "✓ PASS" if result.passed else "✗ FAIL"
        print(f"  {status}")
        print(f"  actual:   {result.actual}")
        print(f"  expected: {result.expected}")
        for ev in result.evidence:
            print(ev)
        results.append((check, result))

    # Summary
    passed = sum(1 for _, r in results if r.passed)
    total = len(results)

    if args.json:
        out = {
            "passed": passed,
            "total": total,
            "results": [
                {
                    "id": c.id,
                    "title": c.title,
                    "passed": r.passed,
                    "actual": r.actual,
                    "expected": r.expected,
                    "evidence": r.evidence,
                }
                for c, r in results
            ],
        }
        print("\n" + json.dumps(out, indent=2, ensure_ascii=False))
    else:
        print("\n" + "=" * 60)
        print(f"AUDIT GUARD: {passed}/{total} checks passed")
        print("=" * 60)

    return 0 if passed == total else 1


if __name__ == "__main__":
    sys.exit(main())
