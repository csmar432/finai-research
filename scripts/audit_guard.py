"""audit_guard.py — Defensive guard against LLM-generated audit hallucinations.

This script verifies audit claims BEFORE any human acts on them. It
runs cheap, deterministic checks (grep, file existence, test runs) that
catch the most common LLM-hallucination patterns:

  - Claiming a file exists that doesn't (e.g., "v1.8.5" in pyproject)
  - Claiming code is missing when it exists (e.g., "0 assert_allclose")
  - Claiming tests fail when they pass
  - Reporting stale numbers (e.g., coverage %)
  - Misreading API signatures

Usage:
    python scripts/audit_guard.py                      # run all checks
    python scripts/audit_guard.py --check 1,3,5        # specific checks
    python scripts/audit_guard.py --update-baseline    # refresh expected state
    python scripts/audit_guard.py --json               # machine-readable output

Exit code: 0 = all claims hold; 1 = at least one claim failed verification.

Design principle
----------------
Audit reports generated by LLMs frequently contain confident-sounding
claims that turn out to be false on inspection ("hallucinated fixes").
We accept that LLMs are useful for *finding* potential issues, but
NEVER trust them to verify their own findings. This script enforces
"show me the evidence" before any change is committed.
"""

