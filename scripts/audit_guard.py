def _grep_count(pattern: str, glob: str, root: Path = PROJECT_ROOT) -> tuple[int, list[str]]:
    """Count files matching pattern; return (count, list_of_paths)."""
    cmd = ["grep", "-rln", "--include", glob, "--exclude-dir=.git", pattern, str(root)]
    res = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
    paths = [p for p in res.stdout.strip().split("\n") if p]
    # Exclude audit-workflow.md (documents the v1.8.5 hallucination example)
    paths = [p for p in paths if not p.endswith("audit-workflow.md")]
    return len(paths), paths


def check_1_pypi_package_exists() -> CheckResult:
    """Audit claim: 'PyPI package finai-research-workflow doesn't exist.'

    We verify by hitting PyPI's JSON API.
    """
    import urllib.request
    import urllib.error

    evidence = []
    try:
        for name in ["finai-research-workflow", "finai_research_workflow"]:
            try:
                urllib.request.urlopen(
                    f"https://pypi.org/pypi/{name}/json", timeout=5
                )
                evidence.append(f"  PyPI {name}: EXISTS")
                return CheckResult(
                    passed=False,
                    actual="exists",
                    expected="not exists",
                    evidence=evidence,
                )
            except urllib.error.HTTPError:
                evidence.append(f"  PyPI {name}: 404 (not found)")
            except Exception as e:
                evidence.append(f"  PyPI {name}: error {e}")
        return CheckResult(
            passed=True,
            actual="not found",
            expected="not found",
            evidence=evidence,
        )
    except Exception as e:
        return CheckResult(
            passed=False,
            actual=f"error: {e}",
            expected="verifiable",
            evidence=[str(e)],
        )


def check_2_version_consistency() -> CheckResult:
    """Audit claim: 'README says v1.8.5 but pyproject is 0.1.0.'

    We grep user-facing docs (README*, pyproject, setup.cfg, __init__.py)
    for version references and report counts. Internal docs are excluded
    because they may legitimately discuss version history.
    """
    files_to_check = ["README.md", "README_EN.md", "pyproject.toml", "setup.cfg", "setup.py"]
    v185_count = 0
    v185_paths = []
    for f in files_to_check:
        p = PROJECT_ROOT / f
        if not p.exists():
            continue
        cmd = ["grep", "-n", "v1\\.8\\.5", str(p)]
        res = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
        lines = [m for m in res.stdout.strip().split("\n") if m]
        v185_count += len(lines)
        v185_paths.extend(f"{f}:{m.split(':', 1)[1]}" for m in lines)

    pyproject_version = "?"
    pyproject_path = PROJECT_ROOT / "pyproject.toml"
    if pyproject_path.exists():
        text = pyproject_path.read_text()
        m = re.search(r'^version\s*=\s*"([^"]+)"', text, re.MULTILINE)
        if m:
            pyproject_version = m.group(1)

    evidence = [
        f"  pyproject.toml version: {pyproject_version}",
        f"  'v1.8.5' in user-facing files: {v185_count}",
    ]
    if v185_paths:
        evidence.extend(f"    → {p}" for p in v185_paths[:5])

    if v185_count == 0:
        return CheckResult(
            passed=True,
            actual=f"0 v1.8.5 references, pyproject={pyproject_version}",
            expected="consistent",
            evidence=evidence,
        )
    return CheckResult(
        passed=False,
        actual=f"{v185_count} v1.8.5 references in user-facing files",
        expected="0 references",
        evidence=evidence,
    )


def check_3_numerical_tests_exist() -> CheckResult:
    """Audit claim: 'Tests lack numerical correctness — 0 assert_allclose.'

    We grep tests/ for assert_allclose calls.
    """
    cmd = ["grep", "-rn", "assert_allclose\\|assert_almost_equal", "tests/"]
    res = subprocess.run(cmd, capture_output=True, text=True, timeout=30)
    matches = [m for m in res.stdout.strip().split("\n") if m and not m.endswith(".pyc")]
    count = len(matches)
    evidence = [f"  assert_allclose/assert_almost_equal calls: {count}"]
    if matches:
        evidence.extend(f"    → {m}" for m in matches[:5])

    if count > 0:
        return CheckResult(
            passed=True,
            actual=f"{count} calls found",
            expected=">0 calls",
            evidence=evidence,
        )
    return CheckResult(
        passed=False,
        actual="0 calls",
        expected=">0 calls",
        evidence=evidence,
    )


def check_4_coverage_threshold() -> CheckResult:
    """Audit claim: 'CI sets fail_under=6 (~6%).'

    We grep ci.yml for the actual value.
    """
    ci_path = PROJECT_ROOT / ".github" / "workflows" / "ci.yml"
    if not ci_path.exists():
        return CheckResult(
            passed=False,
            actual="ci.yml missing",
            expected="ci.yml present",
            evidence=[],
        )
    text = ci_path.read_text()
    m = re.search(r"fail[-_]under=(\d+)", text)
    if not m:
        return CheckResult(
            passed=False,
            actual="no fail-under directive",
            expected="fail-under=N",
            evidence=[],
        )
    val = int(m.group(1))
    evidence = [f"  fail-under={val}"]
    return CheckResult(
        passed=val >= 10,
        actual=f"fail-under={val}",
        expected="≥10 (after audit fix)",
        evidence=evidence,
    )


