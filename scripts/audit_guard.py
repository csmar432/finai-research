    if res.returncode == 0:
        return CheckResult(
            passed=True,
            actual=f"all pass",
            expected="all pass",
            evidence=evidence,
        )
    return CheckResult(
        passed=False,
        actual=f"exit={res.returncode}",
        expected="exit=0",
        evidence=evidence,
    )


